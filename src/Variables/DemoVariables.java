package Variables;

public class DemoVariables {
    public static void main(String[] args) {
        System.out.println("Let's Understand Variables in JAVA");
        //syntex : DataType var_name = Value;


        String City = "AGRA";

        String Name = "Harsh Vardhan";

        String Designation = "Computer Operator";

        double Experience = 6.5;

        int Salary = 25000;

        System.out.println("My Name Is = " +Name);
        System.out.println("Current city where I'm working in = " +City);
        System.out.println("My Current Designation is = " +Designation);
        System.out.println("Total Experience = " +Experience);
        System.out.println("I got Salary = " +Salary);



    }
}
