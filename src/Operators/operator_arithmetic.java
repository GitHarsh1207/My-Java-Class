package Operators;

public class operator_arithmetic {
    public static void main (String[] args) {
        // This is the first program to test arithmetic operations

        int a = 5, b = 6;
        double sum = a + b, sub = a - b, mul = a * b, div = a / b;

        System.out.println("The sum of the values of a & b = "+sum);
        System.out.println("The sub of the values of a & b = "+sub);
        System.out.println("The mul of the values of a & b = "+mul);
        System.out.println("The div of the values of a & b = "+div);

        // 2nd way to print the values of A & B

        System.out.println("------2nd way to print the values of A & B------ ");
        System.out.println("The sub of the value of a & b = "+(a+b));
        System.out.println("The sub of the value of a & b = "+(a-b));
        System.out.println("The sub of the value of a & b = "+(a*b));
        System.out.println("The sub of the value of a & b = "+(a/b));

        double x = 5.7, y = 6.9;


        System.out.println("The sum of the values of x & y = "+sum);
        System.out.println("The sub of the values of x & y = "+sub);
        System.out.println("The mul of the values of x & y = "+mul);
        System.out.println("The div of the values of x & y = "+div);

        // 2nd way to print the values of A & B

        System.out.println("------2nd way to print the values of X & Y--------");
        System.out.println("The sub of the value of x & y = "+(x+y));
        System.out.println("The sub of the value of x & y = "+(x-y));
        System.out.println("The sub of the value of x & y = "+(x*y));
        System.out.println("The sub of the value of x & y = "+(x/y));
        System.out.println("The sub of the value of x & y = "+(x%y));


    }

}
