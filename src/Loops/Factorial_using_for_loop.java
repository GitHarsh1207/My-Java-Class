package Loops;

public class Factorial_using_for_loop {
    public static void main(String[] args) {
        int i;
        int fact=10;
        System.out.println("The Factorial of 5");
        for(i=fact;i<=1;i--);
        {
            System.out.println("The Factorial of 5 = " +fact*i);

        }

    }
}
